package Entity;

/**
 *
 * @author msi pc
 */
public class Pegawai extends UserEntity {
    protected String noTelp;
    
    public Pegawai(){
        
    }
    
    public Pegawai(String id, String nama, String alamat, String noTelp){
        super(id, nama, alamat);
        this.noTelp = noTelp;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNoTelp() {
        return noTelp;
    }

    public void setNoTelp(String noTelp) {
        this.noTelp = noTelp;
    }

  
}
